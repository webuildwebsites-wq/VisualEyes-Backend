function buildFFCells() {
  const addCols = [];
  for (let a = 0.25; a <= 4.0 + 1e-9; a = Math.round((a + 0.25) * 100) / 100) {
    addCols.push(Math.round(a * 100) / 100);
  }

  const cells = [];
  for (const [sph, addMap] of FF_ROWS) {
    for (const add of addCols) {
      cells.push({
        sphere:    sph,
        axisValue: add,
        baseCurve: addMap[add] ?? null,
      });
    }
  }
  return cells;
}

function buildRxSvCells() {
  const cylCols = [];
  for (let c = 0; c <= 6.0 + 1e-9; c = Math.round((c + 0.25) * 100) / 100) {
    cylCols.push(Math.round(c * 100) / 100);
  }

  const cells = [];
  for (const [sph, bc] of RXSV_ROWS) {
    for (const cyl of cylCols) {
      cells.push({ sphere: sph, axisValue: cyl, baseCurve: bc });
    }
  }
  return cells;
}

async function main() {
  await mongoose.connect(process.env.MONGODB_URL);
  console.log("✅ Connected to MongoDB");

  await mongoose.connection.db.dropCollection("lenswaregrids").catch(() => {});
  console.log("🗑️  Dropped existing lenswaregrids collection");

  const ffCells   = buildFFCells();
  const rxSvCells = buildRxSvCells();

  await LenswareGrid.create([
    { gridType: "FFGrid",   cells: ffCells   },
    { gridType: "RxSvGrid", cells: rxSvCells },
  ]);

  console.log(`✅ FFGrid   seeded — ${ffCells.length} cells`);
  console.log(`✅ RxSvGrid seeded — ${rxSvCells.length} cells`);

  // Sanity checks
  const ffDoc   = await LenswareGrid.findOne({ gridType: "FFGrid"   }).lean();
  const rxsvDoc = await LenswareGrid.findOne({ gridType: "RxSvGrid" }).lean();

  const ffTest   = ffDoc.cells.find(c => c.sphere === 3.50 && c.axisValue === 0.25);
  const rxsvTest = rxsvDoc.cells.find(c => c.sphere === 5.00 && c.axisValue === 0.00);

  console.log(`\n🔍 FF    sph=3.50, add=0.25  → baseCurve: ${ffTest?.baseCurve}   (expected: 4)`);
  console.log(`🔍 RxSv  sph=5.00, cyl=0.00  → baseCurve: ${rxsvTest?.baseCurve}  (expected: 7)`);

  await mongoose.disconnect();
  process.exit(0);
}

main().catch((err) => {
  console.error("❌ Fatal:", err);
  process.exit(1);
});
